            <div class="title">Profile</div>
            <p>Supertech Limited, India's leading real estate developer was founded 27 years back in National Capital Region and since then has been scaling new heights by each passing day. The company has set new trends of architectural finesse in the contemporary global scenario touching the horizons of excellence. Established under the dynamic leadership of Mr. R. K. Arora, Supertech has led to creation of various landmark projects. The leaders and skilled professionals of the company have worked towards launching out of the league projects and take the real estate sector to the next level. Supertech is the pioneer to launch the concept of mixed-use development in India and to come up with high rise constructions in North India.</p>
            <div class="subtitle1">PRESENCE</div>
            <p>In North, Supertech has a major presence in Uttrakhand, Uttar Pradesh and Haryana and down South; it is constructing a project in Bengaluru.</p>
            <p>The company is developing projects in different verticals of real estate like residential, townships, commercial, retail, office spaces and hospitality.</p>
            <div class="subtitle1">TECHNOLOGY</div>
            <p>Supertech has not only expanded in terms of number of projects but in terms of the kind of engineering techniques, innovative designing and architectural finesse it has brought in to the Indian real estate industry.</p>
            <p>Supertech is the first Indian real estate developer to introduce the precast technology, which is a step further in construction. With the use of this technology, a hindrance-free construction can be carried out in any season saving up to 64% of time as compared to the construction time of a brick house. It has also introduced the jump form technology which fastens the construction process of any high rise building. The company is also developing projects like North Eye and Supernova in Noida, Uttar Pradesh which are North India's tallest residential and mixed used developments.</p>
